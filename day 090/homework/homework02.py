# 1. მარტივი Swap (ცვლადის გაცვლა):

x, y = 1, 2
x, y = y, x
# x = 2, y = 1



point = (4, 7)
x, y = point
# x = 4, y = 7



person = ("Alice", 25)
name, age = person
# name = "Alice", age = 25